// Write a basic Java program with a main method that prints the sum of two numbers.

public class Exercise1 {

    public static void main(String[] args) {
        // Declare two numbers
        int num1 = 5;
        int num2 = 7;

        // Calculate the sum of the two numbers
        int sum = num1 + num2;

        // Print the sum
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
